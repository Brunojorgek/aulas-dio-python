#Exemplo de declaração de listas
frutas = ["laranja", "maca", "uva"]
print(frutas)

frutas = [] #Também é possível declarar listas vazias.
print(frutas)

letras = list("Python")
print(letras)

numeros = list(range(10))
print(numeros)

carro = ["Ferrari", "F8", 4200000, 2020, "São Paulo", True]
print(carro)